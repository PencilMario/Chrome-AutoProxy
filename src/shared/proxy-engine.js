function normalizeRules(rules = {}) {
  return {
    direct: normalizeRuleList(rules.direct),
    proxy: normalizeRuleList(rules.proxy)
  };
}

function normalizeRuleList(list = []) {
  return list
    .map((rule) => {
      if (typeof rule === "string") return { pattern: normalizeRulePattern(rule) };
      return { ...rule, pattern: normalizeRulePattern(rule?.pattern) };
    })
    .filter((rule) => rule.pattern);
}

function normalizeRulePattern(rule) {
  const pattern = String(rule || "").trim().toLowerCase().replace(/\.$/, "");
  if (pattern.startsWith("*.")) return `*.${hostToAscii(pattern.slice(2))}`;
  return hostToAscii(pattern);
}

function findMatchingRule(host, rules) {
  return rules.find((rule) => matchHostRule(host, rule.pattern));
}

function getRulePattern(rule) {
  return typeof rule === "string" ? normalizeRulePattern(rule) : normalizeRulePattern(rule?.pattern);
}

function selectProxy(proxies = [], proxyId) {
  if (!Array.isArray(proxies) || proxies.length === 0) return null;
  return proxies.find((proxy) => proxy.id === proxyId) || proxies[0];
}

function normalizeCountry(country) {
  const value = String(country || "").trim().toUpperCase();
  return /^[A-Z]{2}$/.test(value) ? value : "";
}

function hostToAscii(host) {
  if (!host || host.includes("*") || host.includes("/") || isIpv4(host)) return host;
  try {
    return new URL(`http://${host}`).hostname || host;
  } catch {
    return host;
  }
}

function stripHostPort(host) {
  if (!host) return "";
  try {
    return new URL(`http://${host}`).hostname;
  } catch {
    if (!host.startsWith("[") && host.includes(":") && host.indexOf(":") === host.lastIndexOf(":")) {
      return host.split(":")[0];
    }
    return host;
  }
}

function asciiSafeTemplate(value) {
  return value.replace(/[^\x00-\x7F]/g, (char) => {
    return `\\u${char.charCodeAt(0).toString(16).padStart(4, "0")}`;
  });
}

function isIpv4(value) {
  const parts = String(value).split(".");
  if (parts.length !== 4) return false;
  return parts.every((part) => {
    const number = Number(part);
    return /^\d+$/.test(part) && number >= 0 && number <= 255;
  });
}

function ipv4ToInt(ip) {
  return ip.split(".").reduce((value, part) => ((value << 8) + Number(part)) >>> 0, 0);
}

function isIpv4InRange(value, start, end) {
  return value >= ipv4ToInt(start) && value <= ipv4ToInt(end);
}

function normalizeCountryMap(hostCountries) {
  return Object.fromEntries(
    Object.entries(hostCountries)
      .map(([host, country]) => [normalizeHost(host), normalizeCountry(country)])
      .filter(([host, country]) => host && country)
  );
}

function normalizeGeoIpRecords(records) {
  const hostCountries = {};
  const cidrs = [];

  for (const [host, country] of Object.entries(records || {})) {
    const normalizedHost = normalizeHost(host);
    const normalizedCountry = normalizeCountry(country);
    if (!normalizedHost || !normalizedCountry) continue;

    if (normalizedHost.includes("/")) {
      cidrs.push({ cidr: normalizedHost, country: normalizedCountry });
    } else {
      hostCountries[normalizedHost] = normalizedCountry;
    }
  }

  return { hostCountries, cidrs };
}

function escapeRegExp(value) {
  return value.replace(/[.+?^${}()|[\]\\]/g, "\\$&");
}
