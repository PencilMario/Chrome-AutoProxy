const PROXY_TYPE_MAP = {
  HTTP: "PROXY",
  HTTPS: "HTTPS",
  SOCKS4: "SOCKS",
  SOCKS5: "SOCKS5"
};
const BUILT_IN_DIRECT_RULES = [
  "*.kis.v2.scr.kaspersky-labs.com",
  "trafficlight.bitdefender.com",
  "*.trafficlight.bitdefender.com",
  "safeweb.norton.com",
  "search.norton.com",
  "siteadvisor.com",
  "*.siteadvisor.com",
  "*.trustedsource.org"
];

export function normalizeHost(host) {
  const value = String(host || "")
    .trim()
    .toLowerCase()
    .replace(/\.$/, "");
  if (value.includes("/")) return value;
  const normalized = stripHostPort(value).replace(/^\[|\]$/g, "");
  return hostToAscii(normalized);
}

export function matchHostRule(host, rule) {
  const normalizedHost = normalizeHost(host);
  const pattern = normalizeRulePattern(rule);

  if (!normalizedHost || !pattern) return false;

  if (pattern.startsWith("*.")) {
    const suffix = pattern.slice(1);
    return normalizedHost.endsWith(suffix) && normalizedHost.length > suffix.length;
  }

  if (pattern.includes("*")) {
    const escaped = pattern
      .split("*")
      .map(escapeRegExp)
      .join(".*");
    return new RegExp(`^${escaped}$`, "i").test(normalizedHost);
  }

  return normalizedHost === pattern;
}

export function decideProxyRoute(config) {
  const enabled = Boolean(config?.enabled);
  const host = normalizeHost(config?.host);

  if (!enabled) return { action: "direct", reason: "disabled" };
  if (isPrivateHost(host)) return { action: "direct", reason: "private-ip" };
  const builtInDirectRule = findMatchingRule(host, normalizeRuleList(BUILT_IN_DIRECT_RULES));
  if (builtInDirectRule) {
    return {
      action: "direct",
      reason: "built-in-direct-rule",
      matchedRule: getRulePattern(builtInDirectRule)
    };
  }

  const rules = normalizeRules(config?.rules);
  const directRule = findMatchingRule(host, rules.direct);
  if (directRule) {
    return {
      action: "direct",
      reason: "direct-rule",
      matchedRule: getRulePattern(directRule)
    };
  }

  const proxyRule = findMatchingRule(host, rules.proxy);
  if (proxyRule) {
    const proxy = selectProxy(config?.proxies, proxyRule.proxyId || config?.activeProxyId);
    return {
      action: "proxy",
      reason: "proxy-rule",
      matchedRule: getRulePattern(proxyRule),
      proxy
    };
  }

  const geoip = config?.geoip || {};
  if (geoip.mode === "proxyNonLocal") {
    const hostCountry = normalizeCountry(geoip.hostCountry || geoip.hostCountries?.[host]);
    const localCountries = new Set((geoip.localCountries || []).map(normalizeCountry).filter(Boolean));

    if (hostCountry && localCountries.has(hostCountry)) {
      return { action: "direct", reason: "geoip-local", country: hostCountry };
    }

    if (hostCountry && !localCountries.has(hostCountry)) {
      return {
        action: "proxy",
        reason: "geoip-non-local",
        country: hostCountry,
        proxy: selectProxy(config?.proxies, config?.activeProxyId)
      };
    }
  }

  return { action: "direct", reason: "default-direct" };
}

export function formatProxyEndpoint(proxy) {
  if (!proxy) return "DIRECT";

  const type = PROXY_TYPE_MAP[String(proxy.type || "HTTP").toUpperCase()] || "PROXY";
  const host = String(proxy.host || "").trim();
  const port = Number(proxy.port);

  if (!host || !Number.isInteger(port) || port < 1 || port > 65535) {
    throw new Error(`Invalid proxy endpoint: ${JSON.stringify(proxy)}`);
  }

  return `${type} ${host}:${port}`;
}

