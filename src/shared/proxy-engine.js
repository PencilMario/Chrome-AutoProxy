  const proxyEndpoints = Object.fromEntries(proxies.map((proxy) => [proxy.id, formatProxyEndpoint(proxy)]));
  const defaultProxy = activeProxy ? formatProxyEndpoint(activeProxy) : "DIRECT";
  const hostCountries = config?.geoip?.hostCountries || {};
  const normalizedGeoIp = normalizeGeoIpRecords(hostCountries);
  const localCountries = (config?.geoip?.localCountries || []).map(normalizeCountry).filter(Boolean);

